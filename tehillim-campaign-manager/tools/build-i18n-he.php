/**
 * Compile a translations map into a binary MO file.
 *
 * @param array<string,string> $map msgid => msgstr (include '' header).
 * @return string
 */
function compile_mo(array $map) {
    $keys       = array_keys($map);
    $ids        = '';
    $strs       = '';
    $id_meta    = array();
    $str_meta   = array();
    foreach ($keys as $k) {
        $id_meta[] = array(strlen($k), strlen($ids));
        $ids      .= $k . "\0";
    }
    foreach ($keys as $k) {
        $v          = $map[$k];
        $str_meta[] = array(strlen($v), strlen($strs));
        $strs      .= $v . "\0";
    }
    $n          = count($keys);
    $id_table   = 28;
    $str_table  = $id_table + $n * 8;
    $ids_start  = $str_table + $n * 8;
    $strs_start = $ids_start + strlen($ids);

    $out  = pack('V', 0x950412de);
    $out .= pack('V', 0);
    $out .= pack('V', $n);
    $out .= pack('V', $id_table);
    $out .= pack('V', $str_table);
    $out .= pack('V', 0);
    $out .= pack('V', $ids_start);
    foreach ($id_meta as $m) {
        $out .= pack('VV', $m[0], $ids_start + $m[1]);
    }
    foreach ($str_meta as $m) {
        $out .= pack('VV', $m[0], $strs_start + $m[1]);
    }
    $out .= $ids;
    $out .= $strs;
    return $out;
}

/**
 * Escape a string for a .po file.
 *
 * @param string $s String.
 * @return string
 */
function po_escape($s) {
    return str_replace(array("\\", "\"", "\n"), array("\\\\", "\\\"", "\\n"), $s);
}
